    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    Connection con = null;
    PreparedStatement smt = null;
    
    try {
        // Carregar o driver JDBC 
        Class.forName("com.mysql.cj.jdbc.Driver");
        
        // Conectar ao banco de dados
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/calculadora", "root", "");
        
        // Convertendo variáveis para string
        String num1 = Double.toString(this.num1);  
        String num2 = Double.toString(this.num2); 
        String operacao = this.opr; 
        String resultado = Double.toString(this.result);  
        
        // Preparando a inserção no banco de dados
        String sql = "INSERT INTO operacoes(num1, num2, operacao, resultado) VALUES (?, ?, ?, ?)";
        smt = con.prepareStatement(sql);
        smt.setString(1, num1);  
        smt.setString(2, num2);  
        smt.setString(3, operacao);  
        smt.setString(4, resultado);
        
        // Executando a inserção
        smt.executeUpdate(); 
        JOptionPane.showMessageDialog(null, "Operação registrada com sucesso!");
        
    } catch (ClassNotFoundException ex) {
        // Se o driver JDBC não for encontrado
        JOptionPane.showMessageDialog(null, "Driver JDBC não encontrado. Verifique se o driver está no classpath.");
        Logger.getLogger(Calculadora.class.getName()).log(Level.SEVERE, null, ex);
        
    } catch (SQLException ex) {
        // Erro ao conectar ou executar a query no banco de dados
        JOptionPane.showMessageDialog(null, "Erro ao registrar a operação no banco de dados.");
        Logger.getLogger(Calculadora.class.getName()).log(Level.SEVERE, null, ex);
        
    } finally {
        // Fechar a conexão e o statement para evitar vazamentos de memória
        try {
            if (smt != null) smt.close();
            if (con != null) con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Calculadora.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

        
       
        
    }//GEN-LAST:event_jButton7ActionPerformed

