    private void jButton36ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton36ActionPerformed
        // Resultado
        num2 = Double.parseDouble(jTextField2.getText());
        
        if(opr == "+"){
            
            result = num1 + num2;
            jTextField2.setText(Double.toString(result));
        }
        else if(opr == "-"){
     
            result = num1 - num2;
            jTextField2.setText(Double.toString(result));
        
        }
        else if(opr == "*"){
     
            result = num1 * num2;
            jTextField2.setText(Double.toString(result));
        
        }
        else if(opr == "/"){
     
            result = num1 / num2;
            jTextField2.setText(Double.toString(result));
        
        }
        else if(opr == "%"){
     
            result = num1 % num2;
            jTextField2.setText(Double.toString(result));
        
        }
        else if(opr == "x^y"){
            
            for(int i = 0; i <= num2; i++){
                
                result += num1 * num2;
                
            }
            jTextField2.setText(Double.toString(result));
        }
        else if(opr == "ftr"){
            
            result = 1;
            for(int i = 1; i <= num1; i++){
            
                result *= i;
            
            }
            jTextField2.setText("");
            jTextField2.setText(Double.toString(result));
        
        }
        
    }//GEN-LAST:event_jButton36ActionPerformed

